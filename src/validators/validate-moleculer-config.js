import path from 'path'
import { exists, readJsonFile } from '../utils/fs-helpers.js'
import { AppError } from '../errors/AppError.js'
import { logger } from '../utils/logger.js'

/**
 * Validates .moleculer-gen/config.json.
 * Throws only when validation cannot continue.
 *
 * @param {string} projectDir
 * @returns {Promise<{ valid: boolean, errors: string[], warnings: string[], config: object }>}
 */
export const validateMoleculerConfig = async projectDir => {
  const errors = []
  const warnings = []

  const moleculerConfigPath = path.join(projectDir, '.moleculer-gen/config.json')

  if (!await exists(moleculerConfigPath)) {
    throw new AppError(
      'Missing .moleculer-gen/config.json. Are you inside a project generated by moleculer-gen?',
      { code: 'CONFIG_NOT_FOUND' }
    )
  }

  const moleculerConfig = await readJsonFile(moleculerConfigPath)

  if (!moleculerConfig || typeof moleculerConfig !== 'object' || Array.isArray(moleculerConfig)) {
    throw new AppError(
      'Invalid .moleculer-gen/config.json: expected an object',
      { code: 'INVALID_CONFIG' }
    )
  }

  if (!moleculerConfig.projectName || typeof moleculerConfig.projectName !== 'string') {
    errors.push('Invalid .moleculer-gen/config.json: missing or invalid "projectName"')
  }

  if (!moleculerConfig.projectNameSanitized || typeof moleculerConfig.projectNameSanitized !== 'string') {
    errors.push('Invalid .moleculer-gen/config.json: missing or invalid "projectNameSanitized"')
  }

  if (!moleculerConfig.database || typeof moleculerConfig.database !== 'string') {
    errors.push('Invalid .moleculer-gen/config.json: missing or invalid "database"')
  }

  if (!moleculerConfig.transporter || typeof moleculerConfig.transporter !== 'string') {
    errors.push('Invalid .moleculer-gen/config.json: missing or invalid "transporter"')
  }

  if (moleculerConfig.plugins !== undefined && !Array.isArray(moleculerConfig.plugins)) {
    errors.push('Invalid .moleculer-gen/config.json: "plugins" must be an array')
  }

  if (
    moleculerConfig.services !== undefined &&
    (
      moleculerConfig.services === null ||
      typeof moleculerConfig.services !== 'object'
    )
  ) {
    errors.push('Invalid .moleculer-gen/config.json: "services" must be an object or an array')
  }

  if (errors.length === 0) {
    logger.info('.moleculer-gen/config.json structure is valid')
  }

  return {
    valid: errors.length === 0,
    errors,
    warnings,
    config: moleculerConfig
  }
}
